
export enum MessageStripType {

    /**
     * Adds an "Success" button to the message Strip.
     */
    SUCCESS = "Success",

    /**
     * Adds an "Information" button to the message Strip.
     */
    INFORMATION = "Information",

    /**
     * Adds an "Warning" button to the message Strip.
     */
    WARNING = "Warning",

    /**
     * Adds an "Error" button to the message Strip.
     */
    ERROR = "Error",
}